def zero_pad(str,zero_num):
	#********************************
	# 数字の文字列を右寄せで指定の
	# 指定の文字数分０埋めする。
	# 
	# str:数字の文字列
	# zero_num:0埋めする文字数
	#********************************
	s_zero = str.zfill(zero_num)

	return s_zero
